//3. Write a program to swap values of two int variables.
package Assignment02;
public class Assignment02_03 {
    public static void main(String []args){
        int a=2, b=3, c;
        System.out.println("a="+a+" b="+b);
        c=a;
        a=b;
        b=c;
        System.out.println("a="+a+" b="+b);
    }
}
